# 1) print() -> prints output to console
print("Hello, this is print()")

# 2) len() -> returns the length of something (string/list/etc)
my_list = [10, 20, 30]
print("Length of list:", len(my_list))

# 3) type() -> tells what data type something is
value = 99.5
print("Type of value:", type(value))

# 4) sum() -> adds all numbers inside a list
print("Sum of list:", sum(my_list))

# 5) sorted() -> new function -> returns new sorted version of a list (does not change original)
# this is built-in too and we haven't used it before
unsorted_list = [5, 2, 9, 1]
print("Sorted list:", sorted(unsorted_list))